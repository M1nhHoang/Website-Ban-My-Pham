package Entity;

/**
 *
 * @author HP
 */
public class Cart {
    private int cart_id;
    private User user;

    public Cart(int cart_id, User user) {
        this.cart_id = cart_id;
        this.user = user;
    }

    public int getCart_id() {
        return cart_id;
    }

    public void setCart_id(int cart_id) {
        this.cart_id = cart_id;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    @Override
    public String toString() {
        return "Cart{" + "cart_id=" + cart_id + ", user=" + user + '}';
    }
}